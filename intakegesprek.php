<p>Procedure:</p>
Het gesprek is al in de geest van hoe wij met onze studenten willen werken: het initiatief zoveel mogelijk bij de student laten en kijken wat er gebeurt. Reageren ze vlot en adequaat.
Als dat niet zo is, het daar tijdens het gesprek over hebben!
Tijdens het stellen van de vragen, leg je op natuurlijke wijze ook iets uit over 'hoe we werken'. Dit komt met de vraag over de vaardigheden nog het sterkst tot uiting.
<ol>
    <li>Je nodigt de student uit om jou uit te nodigen voor een gesprek van een half uur; dit kan snel door op de link van het e-mailadres te klikken, je moet alleen nog je handtekening plaatsen en je beschikbaarheid aangeven; als je de uitnodiging gedaan hebt, kun je dat aangeven onder de kop 'Uitgenodigd'</li>
    <li>De student prikt een moment in je agenda, dit lukt niet altijd in 1 keer; let op de communicatie en zeg daar wat van, ook in positieve zin; als de datum voor het gesprek vaststaat, kun je die invoeren in het scherm als je op het volgnr. voor de student klikt</li>
    <li>De meeting vindt plaats: let op de presentatie (bij een Teams-meeting: is de camera aan?)</li>
    <li>Klik op het volgnr voor de student, het volgende scherm is je leidraad voor het gesprek (volg het van links naar rechts)
        <ul>
            <li>De vooropleiding spreekt voor zich, moet toereikend zijn; vergeet niet onderaan het niveau nog aan te klikken (nodig voor de statistieken)</li>
            <li>Nog andere intakes? Zo ja, waar en waar hangt de keuze nog van af?</li>
            <li>Thuissituatie: (bijv. bij scheiding) geeft dat nog gedoe / spanningen</li>
            <li>Vrije tijd en baantjes: heoveel tijd slokt dat op? Als iemand aangeeft dat hij/zij gamet, hier aandacht aan besteden bij de volgende vraag!</li>
            <li>Verwachtingen: 'een hardcore gamer is nog lang geen SD-liefhebber'</li>
            <li>Toekomstbeeld: spreekt voor zich</li>
            <li>Bijzonderheden: is vaak al gescreend door LBC-er, maar besteed daar wel even aandacht aan</li>
            <li>Voorkennis: vraag over elk begrip of hij/zij kan vertellen wat het is; scoor dat op een driepuntsschaal: -, -/+ en +; vergeet niet onderaan het voorkennis-niveau nog aan te klikken (nodig voor de statistieken)</li>
            <li>Generieke vakken: geef aan dat er nog steeds verplichte vakken zijn; ook bij een MBO-studie doe je niet alleen dingen waar je voor gekozen hebt; hoe staat de student tegenover de generieke vakken?</li>
            <li>Vaardigheden: maak duidelijk dat we geen stamp-opleiding zijn (alles over een programmeertaal leren ofzo); het gaat om 'je zelf kunnen redden in een steeds aan verandering onderhevige SD-omgeving';
                daartoe moet je een aantal belangrijke vaardigheden ontwikkelen; vraag: geef een top 3 (meer mag ook) van vaardigheden aan waarvan je vindt dat die nu al bij je passen (zet daar een plusje achter)</li>
                'Als veel vaardigheden je afschrikken, moet je een andere studiekeuze maken'
        </ul>
    </li>
    <li>Sluit het gesprek af met:
    <ul>
        <li>heeft de student nog vragen? Veel voorkomende vragen:
        <ul>
            <li>boeken-/licentiekosten (licenties Nederlands, rekenen en SPL - ca. &euro; 200)</li>
            <li>laptop specificaties</li>
            <li>aantal meisjes (bij vrouwelijke aanmelding)</li>
            <li>klassegrootte</li>
            <li>versnellingsmogelijkheden</li>
        </ul>
        </li>
        <li>wat gebeurt er vanaf nu? (OWO wordt opgestuurd, ondertekenen en terugsturen; dan zit de student in de molen en volgt de uitnodiging voor de introductie-activiteiten)</li>
        <li>als er nog vragen komen, laat hem/haar dan rechtstreeks contact opnemen met jou</li>
        <li>geef aan wat je van het gesprek vond en of je denkt dat het een goede keus is om deze studie te gaan volgen</li>
    </ul>
    </li>
    <li>Vul tot slot het aqua-blauwe deel van het scherm in: is bepalend!</li>
    <li>Klik op de knop 'Bewaar'</li>
</ol>
